package Practice;

public class C04_ForLoop {

	public static void main(String[] args) {
		//Bir Array içerisindeki verileri for döngüsü kullanarak consola yazdıran bir program.
		
		String arr[]= {"Muhammed","Sebahat","Zeynep","Kemal"};
		
		
		for (int i = 0; i < arr.length; i++) {
			System.out.println(arr[i]);
		}
			
		

	}

}
